//Operator in C++ language:-
//Assignment Operator(+=,-=,*=,/=,%=)
#include <iostream>
using namespace std;
int main() {
    int a=5,b=22;
    cout<<"before assignment";
    cout<<"\na="<<a;
    cout<<"\nb="<<b;
    a+=15;
    b-=10;
    cout<<"\nafter assignment";
    cout<<"\na="<<a;
    cout<<"\nb="<<b;
    return 0;
}
//assignment operator is used to assing value and change the value
