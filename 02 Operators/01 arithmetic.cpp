//Operator in C++ language:-
//Arithmetic Operator(+,-,*,/,%)
#include <iostream>
using namespace std;
int main() {
    int a=9,b=3,Sum,difference,multiply;
    Sum=a+b;
    difference=a-b;
    multiply=a*b;
    cout<<"a="<<a;
    cout<<"\nb="<<b;
    cout<<"\nSum="<<Sum;
    cout<<"\ndifference="<<difference;
    cout<<"\nmultiply="<<multiply;
    return 0;
}
